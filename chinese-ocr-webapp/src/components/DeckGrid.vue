<template>
	<div id='deck-grid'>
		<div class="opacity-87 text-xl lg:text-2xl xl:text-3xl font-normal">
			<p v-if='view == "create"'>Create</p>
			<div v-else>
				<p>Edit</p>
				<p class="mt-2 text-lg lg:text-xl xl:text-2xl font-normal">{{ name }}</p>
			</div>
		</div>
		<div class="mt-8">
			<button @click='addWord("add")'
			class='btn btn-teal opacity-87 text-white py-2 px-4 rounded mr-4'>
				<svg class="bi bi-plus" width="1.25em" height="1.25em" viewBox="0 0 16 16" fill="currentColor" xmlns="http://www.w3.org/2000/svg">
					<path fill-rule="evenodd" d="M8 3.5a.5.5 0 01.5.5v4a.5.5 0 01-.5.5H4a.5.5 0 010-1h3.5V4a.5.5 0 01.5-.5z" clip-rule="evenodd"/>
					<path fill-rule="evenodd" d="M7.5 8a.5.5 0 01.5-.5h4a.5.5 0 010 1H8.5V12a.5.5 0 01-1 0V8z" clip-rule="evenodd"/>
				</svg>
			</button>
			<button @click='addWord("subtract")'
			class='btn btn-red opacity-87 text-white py-2 px-4 rounded mr-4'>
				<svg class="bi bi-dash" width="1.25em" height="1.25em" viewBox="0 0 16 16" fill="currentColor" xmlns="http://www.w3.org/2000/svg">
					<path fill-rule="evenodd" d="M3.5 8a.5.5 0 01.5-.5h8a.5.5 0 010 1H4a.5.5 0 01-.5-.5z" clip-rule="evenodd"/>
				</svg>
			</button>
		</div>
		<div class='mt-6'>
			<div class="w-1/2 lg:w-1/4">
				<input v-model='deck.name' class="bg-gray-300 shadow appearance-none rounded w-full py-2 px-4 text-gray-800 
				leading-tight focus:outline-none focus:shadow-outline" type="text" placeholder='Deck name'>
				<p v-if='$store.state.formError.length' class='text-red-300 text-sm mt-1 -mb-1'>{{ $store.state.formError }}</p>
			</div>
			<div class="w-4/5 lg:w-2/5 mt-4 mb-12">
				<input v-model='deck.description' class="bg-gray-300 shadow appearance-none rounded w-full py-2 px-4 text-gray-800 
				leading-tight focus:outline-none focus:shadow-outline" type="text" placeholder='Description'>
			</div>
			<!-- TODO: Add deck language -->
			<div v-for='(n, i) in deck.numOfWords' :key='i.key' >
				<form>
					<div class="flex flex-wrap -mx-3 mb-6">
						<div class="w-1/3 lg:w-1/4 px-3">
							<input v-model='deck.cards[i].pinyin' class="bg-gray-300 shadow appearance-none rounded w-full py-3 px-4 text-gray-800 
							leading-tight focus:outline-none focus:shadow-outline" type="text" :placeholder='(i+1) + ".  huā"'>
						</div>
						<div class="w-1/3 lg:w-1/4 px-3">
							<input v-model='deck.cards[i].hanzi' class="bg-gray-300 shadow appearance-none rounded w-full py-3 px-4 text-gray-800 
							leading-tight focus:outline-none focus:shadow-outline" type="text" placeholder="花">
						</div>
						<div class="w-1/3 lg:w-1/4 px-3">
							<input v-model='deck.cards[i].definition' class="bg-gray-300 shadow appearance-none rounded w-full py-3 px-4 text-gray-800 
							leading-tight focus:outline-none focus:shadow-outline" type="text" placeholder="flower">
						</div>
					</div>
				</form>
			</div>
			<div class='mt-12'>
				<button @click='createDeck'
				class='btn btn-purple opacity-87 text-white py-2 px-4 rounded my-auto'>
					<p v-if='view == "create"'>Create Deck</p>
					<p v-else>Save Changes</p>
				</button>
			</div>
			<div v-if='view == "edit"' class='mt-8'>
				<button @click='deleteDeck'
				class='btn btn-red opacity-87 text-white py-2 px-4 rounded my-auto'>
					Delete Deck
				</button>
			</div>
		</div>
	</div>
</template>

<script>
export default {
	name: 'DeckGrid',
	props: {
		view: String,
		name: String
	},
	data() {
		return {
			deck: {
				name: '',
				numOfWords: 3,			// Default when creating deck
				description: '',
				cards: [],
				ocr: false,
				dueDates: [],				// Used to show user when to review
				nextInterval: [],		// Used to calculate dueDates
				numReviews: [],
				timesCorrect: []
			}
		}
	},
	methods: {
		loadDeck() {
			this.deck = this.$store.state.decks.find(obj => {
				return obj.name == this.name
			})
			if(!this.deck.ocr) { // Allows us to see if deck has the updated properties
				// TODO: Remove if statement for production (all user decks will start empty)
				this.deck.ocr = false
				this.deck.numReviews = this.deck.timesCorrect = this.deck.nextInterval = 
					Array.apply(null, Array(this.deck.numOfWords)).map(Number.prototype.valueOf, 0)
				// TODO: Calculate due dates (use UTC) instead of empty string
				this.deck.dueDates = Array.apply(null, Array(this.deck.numOfWords)).map(String.prototype.valueOf, '')
			}
		},
		pushData(){
			this.deck.cards.push({
				pinyin: '',
				hanzi: '',
				definition: ''
			})
			this.deck.dueDates.push('')
			this.deck.nextInterval.push(0)
			this.deck.numReviews.push(0)
			this.deck.timesCorrect.push(0)
		},
		spliceMetadata(index) {
			this.deck.cards.splice(index, 1)
			this.deck.dueDates.splice(index, 1)
			this.deck.nextInterval.splice(index, 1)
			this.deck.numReviews.splice(index, 1)
			this.deck.timesCorrect.splice(index, 1)
			this.deck.numOfWords--
		},
		resetDeck() {
			for(let i = 0; i < this.deck.numOfWords; i++) {
				this.pushData()
			}
		},
		trimDeck() {
			for(let i = 0; i < this.deck.numOfWords; i++) {
				if (!(this.deck.cards[i].pinyin || this.deck.cards[i].hanzi || this.deck.cards[i].definition)) {
					this.spliceMetadata(i)
				}
			}
		},
		createDeck() {
			this.trimDeck()
			this.$store.commit('addSuccess', '')
			this.$store.dispatch('createDeck', {
				method: this.view,
				name: this.deck.name,
				deck: JSON.parse(JSON.stringify(this.deck))
			})
				.then(() => {
					if (this.view == 'create') {
						this.$store.dispatch('showSuccess', 'Deck created successfully')
					} else {
						this.$store.dispatch('showSuccess', 'Deck edited successfully')
					}
				})
				.catch(error => console.log(error))
		},
		addWord(addSubtract) {
			if (addSubtract == 'add') {
				this.deck.numOfWords++
				this.pushData()
			} else {
				this.spliceMetadata(this.deck.numOfWords - 1)
			}
		},
		deleteDeck() { // TODO: Add confirmation
			this.$store.commit('addSuccess', '')
			this.$store.dispatch('deleteDeck', this.deck.name)
				.then(this.$store.dispatch('showSuccess', 'Deck deleted successfully'))
		}
	},
	created(){
		this.$store.commit('addError', '')
		if (this.view == 'create') {
			this.resetDeck()
		} else {
			this.loadDeck()
		}
	}
}
</script>

